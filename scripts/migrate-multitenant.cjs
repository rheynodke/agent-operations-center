#!/usr/bin/env node
'use strict';
/**
 * One-shot migration to prepare the AOC filesystem for multi-tenant gateway operation.
 *
 * Idempotent. Use --dry-run to preview without writing.
 *
 *   node scripts/migrate-multitenant.cjs --dry-run
 *   node scripts/migrate-multitenant.cjs
 */

const fs   = require('fs');
const path = require('path');

/**
 * Pure helper. Given the parsed `openclaw.json` content, return:
 *   - providers: a config object with apiKey replaced by ${ENV_VAR} refs
 *   - secrets:   array of { envVar, literal, provider } for each replacement
 *
 * @param {object} input - parsed openclaw.json
 * @returns {{providers: object, secrets: Array<{envVar:string, literal:string, provider:string}>}}
 */
function extractProvidersWithEnvRefs(input) {
  const providersIn = input?.models?.providers ?? {};
  const out = {};
  const secrets = [];

  for (const [name, cfg] of Object.entries(providersIn)) {
    const copy = { ...cfg };
    const literal = cfg?.apiKey;

    if (typeof literal === 'string' && literal.length > 0 && !literal.startsWith('${')) {
      const envVar = providerNameToEnvVar(name);
      copy.apiKey = '${' + envVar + '}';
      secrets.push({ envVar, literal, provider: name });
    }

    out[name] = copy;
  }

  return {
    providers: { models: { providers: out } },
    secrets,
  };
}

function providerNameToEnvVar(name) {
  return String(name).toUpperCase().replace(/[^A-Z0-9]/g, '_') + '_API_KEY';
}

const { OPENCLAW_BASE, SHARED_PROVIDERS, readJsonSafe } = require('../server/lib/config.cjs');

const USERS_DIR  = path.join(OPENCLAW_BASE, 'users');
const SHARED_DIR = path.join(OPENCLAW_BASE, 'shared');
const ADMIN_CFG  = path.join(OPENCLAW_BASE, 'openclaw.json');
const SKILLS_DIR = path.join(OPENCLAW_BASE, 'skills');

function logf(prefix, msg) { console.log(`[migrate] ${prefix} ${msg}`); }

function ensureDir(dir, dryRun) {
  if (fs.existsSync(dir)) {
    logf('OK ', `dir exists: ${dir}`);
    return false;
  }
  if (dryRun) {
    logf('DRY', `would create dir: ${dir}`);
  } else {
    fs.mkdirSync(dir, { recursive: true });
    logf('NEW', `created dir: ${dir}`);
  }
  return true;
}

function writeProvidersFile(secrets, providersConfig, dryRun) {
  const json5 = '// Auto-generated by scripts/migrate-multitenant.cjs\n' +
                '// Per-user gateways $include this file for shared model provider config.\n' +
                '// API keys are referenced via ${ENV_VAR} — define them in AOC backend\'s environment.\n\n' +
                JSON.stringify(providersConfig, null, 2) + '\n';

  if (fs.existsSync(SHARED_PROVIDERS)) {
    logf('OK ', `providers file already exists, leaving as-is: ${SHARED_PROVIDERS}`);
    logf('   ', '(re-run with PROVIDERS_OVERWRITE=1 env to force regenerate)');
    if (process.env.PROVIDERS_OVERWRITE !== '1') return;
  }

  if (dryRun) {
    logf('DRY', `would write providers config to: ${SHARED_PROVIDERS}`);
    logf('DRY', `  size: ${json5.length} bytes`);
    logf('DRY', `  secrets to externalize: ${secrets.length}`);
  } else {
    fs.mkdirSync(path.dirname(SHARED_PROVIDERS), { recursive: true });
    fs.writeFileSync(SHARED_PROVIDERS, json5);
    logf('NEW', `wrote providers config: ${SHARED_PROVIDERS}`);
  }
}

function printSecretsBanner(secrets) {
  if (secrets.length === 0) {
    logf('OK ', 'no API keys needed externalization (all empty or already ${VAR}-referenced)');
    return;
  }
  console.log('');
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log('  ACTION REQUIRED: move these API keys to your AOC backend env');
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log('');
  console.log('  Add to .env (or your shell rc) so AOC backend can resolve');
  console.log('  the ${VAR} references in shared/providers.json5:');
  console.log('');
  for (const s of secrets) {
    console.log(`    ${s.envVar}=${s.literal}    # provider: ${s.provider}`);
  }
  console.log('');
  console.log('  After AOC backend restarts with these env vars set, per-user');
  console.log('  gateways will inherit them via process.env.');
  console.log('');
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
}

async function main(argv) {
  const dryRun = argv.includes('--dry-run');
  if (dryRun) console.log('[migrate] DRY RUN — no filesystem writes');

  if (!fs.existsSync(OPENCLAW_BASE)) {
    throw new Error(`OPENCLAW_BASE missing: ${OPENCLAW_BASE} — is OpenClaw installed?`);
  }
  logf('OK ', `OPENCLAW_BASE: ${OPENCLAW_BASE}`);
  if (!fs.existsSync(SKILLS_DIR)) {
    logf('WARN', `skills dir absent (will be created on first OpenClaw use): ${SKILLS_DIR}`);
  } else {
    logf('OK ', `skills dir exists (will serve as shared pool): ${SKILLS_DIR}`);
  }

  ensureDir(USERS_DIR,  dryRun);
  ensureDir(SHARED_DIR, dryRun);

  const adminCfg = readJsonSafe(ADMIN_CFG);
  if (!adminCfg) {
    logf('WARN', `admin openclaw.json missing or unreadable: ${ADMIN_CFG}`);
    logf('   ', 'skipping provider extraction — providers.json5 not generated');
  } else {
    const { providers, secrets } = extractProvidersWithEnvRefs(adminCfg);
    writeProvidersFile(secrets, providers, dryRun);
    printSecretsBanner(secrets);
  }

  console.log('');
  logf('DONE', dryRun ? '(dry run — re-run without --dry-run to apply)' : '(migration applied)');
}

if (require.main === module) {
  main(process.argv.slice(2)).catch((e) => {
    console.error('[migrate] FAILED:', e.message);
    process.exit(1);
  });
}

module.exports = {
  extractProvidersWithEnvRefs,
  providerNameToEnvVar,
};
